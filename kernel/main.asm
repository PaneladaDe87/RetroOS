ORG 0x0001

JP MAIN_LOOP

MAIN_LOOP:
    CP A
    JR Z, INSTALL_BASIC_APPS
    JR NZ, MAIN_LOOP

INSTALL_BASIC_APPS:
    LD B, 1
    LD C, 1
    LD D, 1

    LD A, (B + C + D)

    JP MAIN_LOOP

FILE_MANAGER:
    CP B
    JR Z, NO_APP_INSTALLED
    JR NZ, OPEN_FILE_MANAGER

NO_APP_INSTALLED:
    RET
